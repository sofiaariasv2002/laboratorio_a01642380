# Nueva función para generar un cuadro 4x10 con valores aleatorios
def generar_cuadro_4x10():
    return [[choice([0, 1]) for _ in range(10)] for _ in range(4)]

# Nueva función para insertar el cuadro en la lista tiles
def insertar_cuadro_en_tiles(tiles, cuadro, start_row, start_col, num_columnas):
    start_index = start_col + start_row * num_columnas #Cálculo del Índice de Inicio
#Inserción del Cuadro
    for i in range(4):
        for j in range(10):
            tiles[start_index + i * num_columnas + j] = cuadro[i][j]

# Generar el cuadro 4x10
cuadro = generar_cuadro_4x10()

# Insertar el cuadro en la posición deseada
insertar_cuadro_en_tiles(tiles, cuadro, 2, 5, 20)

def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            # If next position is not valid move to other random side at the same speed
            options = [
                vector(10, 0),
                vector(-10, 0),
                vector(0, 10),
                vector(0, -10),
            ]
          
            # Calculate the best move based on the shortest distance to pacman
            best_move = None
            min_dist = float('inf')
 
            for option in options:
                new_point = point + option
                if valid(new_point):
                    distance = abs(pacman - new_point)
                    if distance < min_dist:
                        min_dist = distance
                        best_move = option

            if best_move:
                course.x = best_move.x
                course.y = best_move.y
 
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

